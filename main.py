def iterateCells(M):
    
    arr = np.array(M)
    result = np.where(arr == 1)
    listOfCoordinates = list(zip(result[1], result[0]))
    
    M_resh = np.reshape(M1, (np.shape(M)[0]*np.shape(M)[1], ))
    
    Ro = []

    for cell, coords in zip(M_resh, listOfCoordinates):
        
        if cell == 1:
            
            vals = computeRo(coords[0]+1, coords[1]+1)
        
            Ro.append(vals)
        
    

           
def computeRo(x, y):
    # angle = 0
    
    vals = []
    
    for angle in L:
        
        R = x * math.cos(math.radians(angle)) + y * math.sin(math.radians(angle))
        
        if R > 0:
            R += 0.5
        elif R < 0:
            R -= 0.5
            
        Ro_val = int(R)

        vals.append(Ro_val)
    return vals           

